import type { AnalysisResult } from "@shared/types";
import { jsPDF } from "jspdf";
import React, { useState } from "react";

interface DownloadPDFProps {
  result: AnalysisResult;
}

export const DownloadPDF: React.FC<DownloadPDFProps> = ({ result }) => {
  const [isGenerating, setIsGenerating] = useState(false);

  const generatePDF = async () => {
    setIsGenerating(true);

    try {
      const doc = new jsPDF();
      const pageWidth = doc.internal.pageSize.getWidth();
      const margin = 20;
      const maxWidth = pageWidth - margin * 2;
      let yPos = 20;
      const lineHeight = 7;

      // Helper function to add wrapped text
      const addWrappedText = (
        text: string,
        fontSize: number,
        isBold = false,
      ) => {
        doc.setFontSize(fontSize);
        doc.setFont("helvetica", isBold ? "bold" : "normal");
        const lines = doc.splitTextToSize(text, maxWidth);

        // Check if we need a new page
        const requiredHeight = lines.length * lineHeight;
        if (
          yPos + requiredHeight >
          doc.internal.pageSize.getHeight() - margin
        ) {
          doc.addPage();
          yPos = 20;
        }

        doc.text(lines, margin, yPos);
        yPos += lines.length * lineHeight;
        return lines.length;
      };

      // Title
      doc.setFontSize(20);
      doc.setFont("helvetica", "bold");
      doc.text("Hiring Signal Analysis Report", margin, yPos);
      yPos += 15;

      // Score
      doc.setFontSize(14);
      doc.setFont("helvetica", "bold");
      doc.text(`Match Score: ${result.matchScore}/100`, margin, yPos);
      yPos += 10;

      // Summary
      addWrappedText("SUMMARY", 12, true);
      yPos += 2;
      addWrappedText(result.summary, 10);
      yPos += 8;

      // Strengths
      if (result.strengths.length > 0) {
        addWrappedText("STRENGTHS", 12, true);
        yPos += 2;
        result.strengths.forEach((item) => {
          addWrappedText(`• ${item.headline}`, 10, true);
          addWrappedText(`  ${item.detail}`, 9);
          yPos += 2;
        });
        yPos += 6;
      }

      // Gaps
      if (result.gaps.length > 0) {
        addWrappedText("GAPS", 12, true);
        yPos += 2;
        result.gaps.forEach((item) => {
          addWrappedText(`• ${item.headline}`, 10, true);
          addWrappedText(`  ${item.detail}`, 9);
          yPos += 2;
        });
        yPos += 6;
      }

      // Improvements
      if (result.improvements.length > 0) {
        addWrappedText("IMPROVEMENT SUGGESTIONS", 12, true);
        yPos += 2;
        result.improvements.forEach((item, i) => {
          addWrappedText(`${i + 1}. ${item.headline}`, 10, true);
          addWrappedText(`   ${item.detail}`, 9);
          yPos += 2;
        });
        yPos += 6;
      }

      // Risk Flags
      if (result.riskFlags.length > 0) {
        addWrappedText("RISK FLAGS", 12, true);
        yPos += 2;
        result.riskFlags.forEach((item) => {
          addWrappedText(`⚠ ${item.headline}`, 10, true);
          addWrappedText(`  ${item.detail}`, 9);
          yPos += 2;
        });
        yPos += 6;
      }

      // Footer
      yPos += 10;
      doc.setFontSize(8);
      doc.setFont("helvetica", "italic");
      const disclaimer =
        "This report is generated by AI and is for informational purposes only. It should not be considered professional career advice.";
      const disclaimerLines = doc.splitTextToSize(disclaimer, maxWidth);

      if (
        yPos + disclaimerLines.length * 4 >
        doc.internal.pageSize.getHeight() - margin
      ) {
        doc.addPage();
        yPos = 20;
      }

      doc.text(disclaimerLines, margin, yPos);

      // Date
      yPos += disclaimerLines.length * 4 + 5;
      doc.text(`Generated: ${new Date().toLocaleDateString()}`, margin, yPos);

      // Save the PDF
      doc.save("hiring-signal-analysis.pdf");
    } catch (error) {
      console.error("Failed to generate PDF:", error);
    } finally {
      setIsGenerating(false);
    }
  };

  return (
    <button
      onClick={generatePDF}
      disabled={isGenerating}
      className={`flex items-center gap-2 text-sm font-medium px-4 py-2 rounded-lg border transition-all ${
        isGenerating
          ? "bg-gray-100 dark:bg-gray-700 text-gray-500 dark:text-gray-400 border-gray-200 dark:border-gray-600 cursor-wait"
          : "bg-white dark:bg-gray-800 text-gray-700 dark:text-gray-300 border-gray-300 dark:border-gray-600 hover:bg-gray-50 dark:hover:bg-gray-700 hover:border-gray-400 dark:hover:border-gray-500"
      }`}
    >
      {isGenerating ? (
        <>
          <svg className="animate-spin w-4 h-4" fill="none" viewBox="0 0 24 24">
            <circle
              className="opacity-25"
              cx="12"
              cy="12"
              r="10"
              stroke="currentColor"
              strokeWidth="4"
            />
            <path
              className="opacity-75"
              fill="currentColor"
              d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
            />
          </svg>
          <span>Generating...</span>
        </>
      ) : (
        <>
          <svg
            className="w-4 h-4"
            fill="none"
            viewBox="0 0 24 24"
            stroke="currentColor"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M12 10v6m0 0l-3-3m3 3l3-3m2 8H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z"
            />
          </svg>
          <span>Download PDF</span>
        </>
      )}
    </button>
  );
};
